package com.foodbarbaz;

public class PhotoHolder {
	private long id;
	private String photo;
	public PhotoHolder() {}
	public long getId() {
		return id;
	}
	public void setId(long id) {
		this.id = id;
	}
	public String getPhoto() {
		return photo;
	}
	public void setPhoto(String photo) {
		this.photo = photo;
	}
	
}
